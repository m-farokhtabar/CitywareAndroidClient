package ir.rayas.app.citywareclient.ViewModel.Marketing;

public class SuggestionInfoViewModel {

    private String Ticket;
    private String TicketValidity;
    private String Percents;

    public String getTicket() {
        return Ticket;
    }

    public void setTicket(String ticket) {
        Ticket = ticket;
    }

    public String getTicketValidity() {
        return TicketValidity;
    }

    public void setTicketValidity(String ticketValidity) {
        TicketValidity = ticketValidity;
    }

    public String getPercents() {
        return Percents;
    }

    public void setPercents(String percents) {
        Percents = percents;
    }
}
